using System;

class Program
{
    static void Main()
    {
        int i = 0;

        // while döngüsü ile 10 kere yazdırma
        while (i < 10)
        {
            Console.WriteLine("Kendime inanıyorum, ben bu yazılım işini hallederim!");
            i++; // i'yi bir artırarak döngü sayısını kontrol ederiz
        }
    }
}
